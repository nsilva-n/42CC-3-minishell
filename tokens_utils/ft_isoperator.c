#include "../minishell.h"

bool	ft_isoperator(char *str, int i)
{
	return ((str[i] == '<' && str[i + 1] == '<')
		|| (str[i] == '>' && str[i + 1] == '>')
		|| str[i] == '|' || str[i] == '>' || str[i] == '<');
}
